package studio.hdr.lms.util;

public class MyQuery {
	private String name;
	private String author;
	private String bookClass;
	private String press;
	private Long countLower;
	private Long countHigher;
	private Double priceLower;
	private Double priceHigher;
	private String description;
	private String lang;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getBookClass() {
		return bookClass;
	}

	public void setBookClass(String bookClass) {
		this.bookClass = bookClass;
	}

	public String getPress() {
		return press;
	}

	public void setPress(String press) {
		this.press = press;
	}

	public Long getCountLower() {
		return countLower;
	}

	public void setCountLower(Long countLower) {
		this.countLower = countLower;
	}

	public Long getCountHigher() {
		return countHigher;
	}

	public void setCountHigher(Long countHigher) {
		this.countHigher = countHigher;
	}

	public Double getPriceLower() {
		return priceLower;
	}

	public void setPriceLower(Double priceLower) {
		this.priceLower = priceLower;
	}

	public Double getPriceHigher() {
		return priceHigher;
	}

	public void setPriceHigher(Double priceHigher) {
		this.priceHigher = priceHigher;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getLang() {
		return lang;
	}

	public void setLang(String lang) {
		this.lang = lang;
	}

}
